#pragma once


class CManager
{
private:
	// static のメンバ変数を作る場合は cpp でもう一度宣言しなおさないといけない
	static class CScene* m_Scene;
public:
	static void Init();
	static void Uninit();
	static void Update();
	static void Draw();

	template<typename T>
	static void SetScene()
	{
		if (m_Scene != NULL)
		{// 現在のシーンを削除
			m_Scene->Uninit();
			delete m_Scene;
		}

		// 新しいシーンを入れる
		T* scene = new T();
		m_Scene = scene;
		scene->Init();
	}

	static CScene* GetScene() { return m_Scene; };
};